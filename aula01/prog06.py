"""
Operadores em Python

OPERADORES DE ATRIBUIÇÃO
São utilizados para atribuir valores que estão à direita em variáveis que estão para à esquerda

"""

if __name__ == "__main__":

    # Atribuído valor que está a direita à variável que está à esquerda
    valor = 100

    # Caso necessário incrementar esse valor, fazer o seguinte:
    valor = valor + 5
    # Porém podemos utilizar formas curtas de atribuição
    valor += 15
    valor -= 50
    valor *= 1.5

    print(valor)